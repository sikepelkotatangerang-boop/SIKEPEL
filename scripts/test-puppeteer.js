/**
 * Script untuk test Puppeteer PDF generation
 * Run: node scripts/test-puppeteer.js
 */

const puppeteer = require('puppeteer');

async function testPuppeteer() {
  console.log('üß™ Testing Puppeteer PDF generation...\n');
  
  let browser = null;
  
  try {
    // Test 1: Launch browser
    console.log('Test 1: Launching Puppeteer browser...');
    const startTime = Date.now();
    
    browser = await puppeteer.launch({
      headless: true,
      args: [
        '--no-sandbox',
        '--disable-setuid-sandbox',
        '--disable-dev-shm-usage',
        '--disable-gpu',
      ],
    });
    
    const launchTime = Date.now() - startTime;
    console.log(`‚úÖ Browser launched in ${launchTime}ms`);
    console.log('');
    
    // Test 2: Create page and generate PDF
    console.log('Test 2: Generating sample PDF...');
    const page = await browser.newPage();
    
    const htmlContent = `
      <!DOCTYPE html>
      <html>
      <head>
        <meta charset="UTF-8">
        <style>
          body {
            font-family: Arial, sans-serif;
            padding: 40px;
          }
          h1 {
            color: #2563eb;
            text-align: center;
          }
          .info {
            margin: 20px 0;
            padding: 15px;
            background: #f3f4f6;
            border-radius: 8px;
          }
          table {
            width: 100%;
            border-collapse: collapse;
            margin: 20px 0;
          }
          th, td {
            border: 1px solid #ddd;
            padding: 12px;
            text-align: left;
          }
          th {
            background-color: #2563eb;
            color: white;
          }
        </style>
      </head>
      <body>
        <h1>Test PDF Generation dengan Puppeteer</h1>
        
        <div class="info">
          <p><strong>Tanggal:</strong> ${new Date().toLocaleDateString('id-ID')}</p>
          <p><strong>Waktu:</strong> ${new Date().toLocaleTimeString('id-ID')}</p>
        </div>
        
        <h2>Informasi Dokumen</h2>
        <table>
          <tr>
            <th>Field</th>
            <th>Value</th>
          </tr>
          <tr>
            <td>Nomor Surat</td>
            <td>001/TEST/2025</td>
          </tr>
          <tr>
            <td>Jenis Dokumen</td>
            <td>SKTM</td>
          </tr>
          <tr>
            <td>Nama Pemohon</td>
            <td>John Doe</td>
          </tr>
          <tr>
            <td>NIK</td>
            <td>1234567890123456</td>
          </tr>
        </table>
        
        <h2>Kelebihan Puppeteer</h2>
        <ul>
          <li>‚úÖ Gratis (tidak perlu API key)</li>
          <li>‚úÖ Full control atas styling</li>
          <li>‚úÖ Kualitas PDF excellent</li>
          <li>‚úÖ Langsung dari HTML ke PDF</li>
          <li>‚úÖ Bisa offline</li>
        </ul>
        
        <div style="margin-top: 40px; text-align: center; color: #666;">
          <p>Generated by Puppeteer</p>
        </div>
      </body>
      </html>
    `;
    
    await page.setContent(htmlContent, {
      waitUntil: 'networkidle0',
    });
    
    const pdfStartTime = Date.now();
    const pdfBuffer = await page.pdf({
      format: 'A4',
      printBackground: true,
      margin: {
        top: '20mm',
        right: '20mm',
        bottom: '20mm',
        left: '20mm',
      },
    });
    
    const pdfTime = Date.now() - pdfStartTime;
    console.log(`‚úÖ PDF generated in ${pdfTime}ms`);
    console.log(`‚úÖ PDF size: ${(pdfBuffer.length / 1024).toFixed(2)} KB`);
    console.log('');
    
    // Test 3: Save PDF to file
    console.log('Test 3: Saving PDF to file...');
    const fs = require('fs');
    const path = require('path');
    
    const outputDir = path.join(process.cwd(), 'test-output');
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true });
    }
    
    const outputPath = path.join(outputDir, `test-puppeteer-${Date.now()}.pdf`);
    fs.writeFileSync(outputPath, pdfBuffer);
    
    console.log(`‚úÖ PDF saved to: ${outputPath}`);
    console.log('');
    
    // Close browser
    await browser.close();
    browser = null;
    
    // Summary
    console.log('üìä Summary:');
    console.log(`  - Browser launch time: ${launchTime}ms`);
    console.log(`  - PDF generation time: ${pdfTime}ms`);
    console.log(`  - Total time: ${launchTime + pdfTime}ms`);
    console.log(`  - PDF size: ${(pdfBuffer.length / 1024).toFixed(2)} KB`);
    console.log('');
    console.log('‚úÖ All tests passed!');
    console.log(`üìÑ Open the PDF: ${outputPath}`);
    
  } catch (error) {
    console.error('‚ùå Error:', error.message);
    console.error(error);
  } finally {
    if (browser) {
      await browser.close();
    }
  }
}

testPuppeteer();
